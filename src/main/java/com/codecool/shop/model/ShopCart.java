public class ShopCart {

    private Integer id;
    private String status;


    public ShopCart() {
        this.id = id;
        this.status = status;
    }

    public int getid() {
        return id;
    }

    public void setid(int id) {
        this.id = id;
    }

    public void setStatus(String status) {
        this.status = status;
    }

    public String getStatus() {
        return status;
    }



}